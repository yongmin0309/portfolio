package vo;

import java.sql.Date;

public class ReVo {
	private int re_code;
	private String re_phone;
	private Date re_date;
	private String re_time;
	private int re_person;
	private String cos_code;
	private String cos_price;
	
	
	public int getRe_code() {
		return re_code;
	}
	public void setRe_code(int re_code) {
		this.re_code = re_code;
	}
	public String getRe_phone() {
		return re_phone;
	}
	public void setRe_phone(String re_phone) {
		this.re_phone = re_phone;
	}
	public Date getRe_date() {
		return re_date;
	}
	public void setRe_date(Date re_date) {
		this.re_date = re_date;
	}
	public String getRe_time() {
		return re_time;
	}
	public void setRe_time(String re_time) {
		this.re_time = re_time;
	}
	public int getRe_person() {
		return re_person;
	}
	public void setRe_person(int re_person) {
		this.re_person = re_person;
	}
	public String getCos_code() {
		return cos_code;
	}
	public void setCos_code(String cos_code) {
		this.cos_code = cos_code;
	}
	public String getCos_price() {
		return cos_price;
	}
	public void setCos_price(String cos_price) {
		this.cos_price = cos_price;
	}
	
	
	
	
	
	
	

}
